// ex. 3.1C java variable example

public class VarExample3 {
  public static void main(String args[]) {
    short x = 10, y = 5, z = 1;    // declares and assigns short variable
    byte B = 17;                  // declares and assigns byte variable
    long w = 23433400000L;        // declares and assigns long variable
    double d = 5.01;              // declares and assigns double variable
    float f = 5.01f;            // declares and assigns float variable
    char ch = 'a';            // declares and assigns char variable
    String s = "Hello World!";      // declares and assigns a String variable
    boolean isDone = false;        // declares and assigns boolean variable

    System.out.println("ch = " + ch);
    System.out.println("isDone = " + isDone);
    System.out.println(s);
  }
}
